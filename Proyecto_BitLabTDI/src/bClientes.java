    public void NombreC(String nomAbus) {
        NomaBus = nomAbus;
        LlenarTabla(NomaBus);
        cont++;
        if (cont > 1) {
            contaC = TableC.getRowCount();
        }
    }

    public int getContaC() {
        return contaC;
    }

    private void LlenarTabla(String noma) {
        try {
            ReiniciarJTable(TableC);
            login connect = new login();
            connect.obtenConexion();
            String sql = "select id_cliente, nombre, direccion from cliente where nombre like '%" + noma + "%' order by id_cliente asc";
            Statement sent = connect.obtenConexion().createStatement();
            ResultSet rs = sent.executeQuery(sql);

            String fila[] = new String[3];
            while (rs.next()) {
                fila[0] = rs.getString("id_cliente");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("direccion");
                modelo.addRow(fila);
            }
            TableC.setModel(modelo);
            rs.close();
            connect.obtenConexion().close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public static void ReiniciarJTable(javax.swing.JTable Tabla) {
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

    private void Cerrar() {
        try {
            this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
            addWindowListener(new WindowAdapter() {
                public void windowClosing(WindowEvent e) {
                    clientes cont = new clientes();
                    cont.setVisible(true);
                    dispose();
                }
            });
            this.setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private MouseListener clickear() {
        MouseAdapter clickea = new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent me) {
                int fila = TableC.getSelectedRow();
                codigoE = (String) modelo.getValueAt(fila, 0);

                clientes cont = new clientes();
                cont.buscar(codigoE);
                cont.setVisible(true);
                cont.habilitarTF();
                cont.btnActivedSearch();
                dispose();
            }
        };
        return clickea;
    }

